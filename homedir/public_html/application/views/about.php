          <h3>About Us</h3>
          <p>Currently housed at Connecticut College, our project is a collaborative, community-based endeavor to resolve historical inequities in the authorship of local histories, create opportunities for cultural tourism, and place the dissemination of heritage information firmly in the hands of local communities.</p>
          <p>Open-source software and open-community, online projects (e.g., Wikipedia) epitomize some of the best efforts to democratize the process by which information is collaboratively created and made available to wider audiences via the Internet. Aligning with this philosophy, we seek to create a sustainable, inclusive program for disseminating heritage-related information.</p>
          <p>CamelTours serves this goal in two ways: (1) by providing a mobile-friendly web-application for viewing multimedia content both online and offline; and (2) providing a simple, user-friendly method for communities to create these mobile multimedia heritage tours.</p>
          <p>This project emerged from a marriage of the technical and design-based expertise of computer science with the goals of community-based anthropology at Connecticut College.</p>
