          <h3>Support Us</h3>
          <p>Our open-source project is: 
          <ol>
  		<li>an important step toward decolonizing heritage management;</li>
  		<li>an effective medium for local communities to share information about their landscapes, sites, and features, among other tangible and intangible attributes of local cultures;</li>
  		<li>a means by which local communities author and voice their own histories.</li>
	  </ol>
	  </p>
          <p>CamelTours is a service offered free of charge by a group of scholars who aim to disrupt the commoditization and politicization of heritage and conservation.  We imagine a future in which communities of storytellers and scholars are enabled with reliable, open-source digital technology to share information with the world. To this end, we welcome support in the form of coding expertise, information exchange, and modest donations to help cover the costs of server providers and other development fees.</p>
          <p>Should you want to support CamelTours or support student-faculty research in the Department of Anthropology and/or the Department of Computer Science at Connecticut College, please contact Anthony P. Graesch (anthony.graesch&lt;at&gt;conncoll.edu).</p>
