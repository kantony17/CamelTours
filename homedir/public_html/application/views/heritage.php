          <h3>Serving Sustainable Heritage Plans</h3>
          <p>The long-term successes of local heritage plans often hinge on frequent assessment and a commitment to anticipating ever-evolving attitudes toward tourism, development, and funding. The types of technologies we choose to integrate into our heritage plans can either advance or hinder these successes.  CamelTours maximally aligns with the goals and demands of crafting sustainable heritage plans by being:</p>
<div class="small-12 medium-12 large-12 columns">
            <ul>
              <li><b>Affordable.</b> In fact, CamelTours is free.  This allows communities to use modest budgets for more important matters, such as facilitating the gathering of tour content.</li>
              <li><b>Robust.</b> CamelTours is streamlined to afford maximal usability in the most remote of places.  Although flashier technologies are available, CamelTours is reliable and predictable, ably serving a desired purpose over and over again.</li>
              <li><b>Nimble.</b> Whereas signage is static, our tours are dynamic and can be updated as often as needed with no cost to tour creators.</li>
              <li><b>Reliable.</b> We understand that printing and placing QR codes signs is an investment of time and resources, and we think it important that our system offers stable URLs for all of your CamelTours.</li>
              <li><b>Usable.</b> Effectively sharing information with visitors to your community is only possible if the users’ are presented with an intuitive interface on their mobile devices. We think we’ve achieved this very design.</li>
            </ul>
