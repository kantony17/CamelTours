          <h3>How can CamelTours serve my community's needs?</h3>
          <p>From the outset, we imagined CamelTours to be an important component of local heritage plans, and we designed it to serve in many capacities.  A CamelTour can be an accompaniment to self-guided walking tours, or it might complement your museum’s exhibits.  It might elaborate on existing signage at roadside vistas, or it might help in the preservation and instruction of your language.  Or it might do all of the above and more.</p>
        </div>
